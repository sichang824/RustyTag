//! # RustyTag
//!
//! `rustytag` 是一个基于 Git 标签的语义化版本管理工具。
//!
//! ## 特性
//!
//! - 基于 Git 标签的语义化版本管理
//! - 完整的语义化版本支持
//! - 自动版本升级（patch/minor/major）
//! - 本地标签与远程仓库同步
//!
//! ## 快速开始
//!
//! ```bash
//! # 初始化语义化版本
//! rustytag init
//!
//! # 升级补丁版本
//! rustytag patch
//! ```
//!
//! ## 命令详解
//!
//! ### 初始化
//!
//! ```bash
//! rustytag init
//! ```
//!
//! 初始化一个新的 Git 仓库并设置语义化版本。
//!
//! ### 版本升级
//!
//! ```bash
//! rustytag patch  # 升级补丁版本 (例如: 1.0.0 -> 1.0.1)
//! rustytag minor  # 升级次要版本 (例如: 1.0.0 -> 1.1.0)
//! rustytag major  # 升级主要版本 (例如: 1.0.0 -> 2.0.0)
//! ```
//!
//! ### 标签同步
//!
//! ```bash
//! rustytag sync   # 同步本地标签与远程仓库
//! rustytag reset  # 重置本地标签以匹配远程仓库
//! ```

